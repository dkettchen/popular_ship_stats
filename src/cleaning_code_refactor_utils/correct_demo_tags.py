def correct_demo_tags(fandom, char, input_tag, gender_or_race):
    """
    assigns any characters that weren't automatically assigned a gender/not assigned the correct one

    returns the new gender if it needed fixing, otherwise it returns the same as passed in
    """

    if gender_or_race == "gender":

        if char in men:
            gender = "M"
        elif char in women:
            gender = "F"
        elif char in other:
            gender = "Other"
        elif char in female_aligned:
            if fandom == "Supernatural":
                gender = "F"
            else:
                gender = "F | Other"
        elif char in male_aligned:
            gender = "M | Other"
        elif char in cis_male_drag_queens:
            gender = "M | F | Other"
        elif char in ambig:
            gender = "Ambig"
        else:
            if input_tag == None:
                print(char)
            gender = input_tag

        return gender
    
    elif gender_or_race == "race":
        # assign base tags
        if char in tag_order_exceptions:
            race_tag = tag_order_exceptions[char]
        elif char in asian_folks:
            race_tag = "Asian"
        elif char in latin_folks:
            race_tag = "Latin"
        elif char in black_folks:
            race_tag = "Black"
        elif char in white_folks:
            race_tag = "White"
        elif char in other_folks:
            race_tag = other_folks[char]
        else: race_tag = input_tag
        
        # retag
        if char in characters_to_retag:
            race_tag = characters_to_retag[char]
        if char == "Ruby" and fandom == "Steven Universe":
            race_tag = "N.H." # not to be confused with supernatural one

        # renaming tags
        if race_tag == "Latino":
            race_tag = "Latin" # making gender neutral
        elif race_tag == "M.E.":
            race_tag = "MENA" # unifying with rest
        elif race_tag == "Roma":
            race_tag = "Romani"
        elif race_tag == "N.H":
            race_tag = "N.H."

        # player characters without customisable looks
        if "Player Character" in char or "Reader" in char:
            if char not in [
                "Amamiya Ren | Joker | Player Character",
                "Byleth Eisner | Player Character",
                "Traveler | Player Character"
            ]:
                race_tag = "Ambig"

        # retag & make more specific

        # sub-categorise asians
        # if they aren't EA, they'll be retagged later
        if race_tag == "Asian" and fandom in east_asian_fandoms or char in east_asian_folks:
            race_tag = "E Asian"
        if char in multi_east_asian_folks:
            race_tag = "E Asian (Multi)"
        # all other asians
        elif char == "Otabek Altin":
            race_tag = "Central As"
        elif char in south_asian_folks:
            race_tag = "S Asian"
        elif char in multi_south_asian_folks:
            race_tag = "S Asian (Multi)"
        elif char in south_east_asian_folks \
        or fandom == "KinnPorsche | คินน์พอร์ช เดอะ ซีรีส์":
            race_tag = "SE Asian"
        elif char in multi_south_east_asian_folks:
            race_tag = "SE Asian (Multi)"
        elif char in multi_asian_folks:
            race_tag = "Asian (Multi)"

        # sub-categorise indigenous folks
